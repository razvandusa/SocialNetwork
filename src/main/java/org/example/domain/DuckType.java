package org.example.domain;

/**
 * Enum representing the different types of ducks in the system.
 * A duck can be capable of flying, swimming, or both.
 */
public enum DuckType {
    /** Duck that can only fly. */
    FLYING,

    /** Duck that can only swim. */
    SWIMMING,

    /** Duck that can both fly and swim. */
    FLYING_AND_SWIMMING
}
